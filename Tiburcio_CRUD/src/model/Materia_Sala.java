package model;

public class Materia_Sala {

	//Objeto associativo de materia e sala
	private String hora;
	private String dia;
	private Materia m;
	private Sala s;
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public String getDia() {
		return dia;
	}
	public void setDia(String dia) {
		this.dia = dia;
	}
	public Sala getS() {
		return s;
	}
	public void setS(Sala s) {
		this.s = s;
	}
	public Materia getM() {
		return m;
	}
	public void setM(Materia m) {
		this.m = m;
	}
}
	

